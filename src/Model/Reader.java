public class Reader {
	private int readID;
	private String name;
	private int age;
	private String tel;
	public int getReadID() {
		return readID;
	}
	public void setReadID(int readID) {
		this.readID = readID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
}
